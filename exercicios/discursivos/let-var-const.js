// Com suas palavras descreva a importância de diferenciar as declarações var, let e const.
// A descrição de cada uma delas deve responder as seguintes perguntas:
// A declaração em questão respeita quais escopos?
// A declaração em questão pode ser atualizada?
// A declaração em questão pode ser declarada mais de uma vez?

// RESPOSTA:
// A declaração em questão pode ser declarada sem inicialização?
// Usamos var em momentos que precisamos atualizar uma váriavel, declarar ela mais de uma vez, declarar ela sem inicialização e respeitar apenas escopo global e de função. Já para let, podemos atualizar seu valor, declarar sem inicialização e seu uso é recomendado ao invés de var pois respeita escopo global, de função e de bloco e não pode ser declarada mais de uma vez no mesmo escopo. A const é utilizada quando seu valor não precisa ser atualizado, ela precisa receber inicialização durante a declara e assim como a let respeita escopo global, de função e de bloco e não pode ser declarada mais de uma vez no mesmo escopo.
