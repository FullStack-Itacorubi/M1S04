// Usando seu entendimento, explique o que cada um dos métodos de array abaixo executam:
// forEach
// map
// filter
// find
// reduce
// every

// RESPOSTA
// forEach: percorre cada item de um array executando uma função para cada
// map: percorre cada item de um array alterando seu valor por meio de uma callback
// filter: a partir de uma função fornecida cria um novo array que passarem no teste lógico
// find: a partir de um teste lógico retorna o valor do primeiro elemento do array que satisfizer a condição
// reduce: executa uma de redução, fornecida como callback, para cada elemento do array e tem como retorno um único valor
// every: testa se todos os itens do array passam por um teste lógico fornecido
